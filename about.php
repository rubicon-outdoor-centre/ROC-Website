                <section id="facilities" class="section">
                    <h2 class="section__title">Facilities</h2>
                    <a class="section__back-to-top" href="#top"><i class="icon-font  icon-font--up-arrow"></i></a>
                    <p>Rubicon has varied accommodation facilities including dormitories, two self-contained units, and Royston House, a hostel high in the Rubicon Ranges. Specialist teaching equipment e.g. skis, rafts, tents and canoes are supplied and internal transport is provided by buses and 4WD vehicles.</p>
                    <p>The Rubicon Campus has a well-equipped laundry, drying room and clothes line for students. The laundry has two washing machines, and a tumble dryer. There is a phone available for the use of visiting teachers and, under certain conditions, students.</p>
                    <p>A bouldering wall, basketball/netball court, beach volleyball court, playing oval, a variety of sports equipment and recreation hall complete with table tennis, pool table and foos ball are available for use at specified times. Students may bring their own musical instruments, games, cards and CD's. A CD player is provided in the Hall for students to use. iPods and other personal listening devices should not be brought to the Centre.</p>
