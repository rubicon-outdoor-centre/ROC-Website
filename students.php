                <section id="code_of_conduct" class="section">
                    <h2 class="section__title">Code of Conduct</h2>
                    <a class="section__back-to-top" href="#top"><i class="icon-font  icon-font--up-arrow"></i></a>
                    <h3>Guiding Principles</h3>
                    <ul>
                        <li><p>To ensure that the rights of all individuals are protected whilst at the Centre, and travelling to and from the Centre.</p></li>
                        <li><p>To create the best possible learning environment.</p></li>
                        <li><p>To ensure that breaches of our code of conduct are treated in a fair and consistent manner and within the expectations of staff and students.</p></li>
                    </ul>
                    <h3>Rights</h3>
                    <ul>
                        <li><p>All individuals and their property are to be valued and treated with <u>respect</u>.</p></li>
                        <li><p>All individuals have the right to a safe, secure and cooperative working environment in which participation, risk taking and confronting challenges are encouraged.</p></li>
                        <li><p>All individuals have the right to privacy.</p></li>
                        <li><p>Staff should be able to perform their duties in an atmosphere of order and with cooperation.</p></li>
                        <li><p>Staff, students and the natural environment have the right to exist in a healthy and environmentally considerate atmosphere.</p></li>
                        <li><p>The Centre expects support from all students, parents, and staff in implementing this Code of Conduct.</p></li>
                    </ul>
                    <h3>Responsibilities</h3>
                    <ul>
                        <li><p>All students should treat one another, staff property and the environment, with <u>respect</u>.</p></li>
                        <li><p>All students should act and behave in a way which does not endanger, intimidate or interfere with the participation of others.</p></li>
                        <li><p>All students should respect others rights to privacy.</p></li>
                        <li><p>Students should comply with instructions given by any members of staff and should willingly contribute to the daily routine of the Centre.</p></li>
                        <li><p>All students should conduct themselves in a safe, healthy and environmentally friendly manner.</p></li>
                        <li><p>All students should behave according to this code and accept the consequences if it is breached. All staff should fairly, reasonably and consistently implement this code. Staff and parents of participating schools should support the Centre in implementing the Code of Conduct.</p></li>
                    </ul>
                    <h3>Rubicon Expectations</h3>
                    <ol>
                        <li><p>Students will comply with the directions of Rubicon Staff.</p></li>
                        <li><p>Fighting, bullying and any other form of aggressive behaviour, including offensive, sexist or racist language, will be seen as a breech of our Code of Conduct.</p></li>
                        <li><p>Students are requested to <u>not</u> leave the Centre, unless they have the permission of the Principal or the Rubicon Teacher on duty.  For reasons of safety, certain areas are out of bounds to all students; these will be outlined upon arrival.</p></li>
                        <li><p>Cigarettes, alcohol, illicit drugs, offensive weapons, and pornographic material must not be brought to the Centre.</p></li>
                        <li><p>Rubicon recommends that personal listening devices and mobile phones are not consistent with the aims of our program. If brought to the Centre, they must not to be used during activity time. The Centre takes no responsibility for any loss or damage to these items.</p></li>
                        <li><p>Students will dress appropriately at all times, including following Sun Smart requirements during Terms 1 and 4; this includes wearing hats and no singlet tops.</p></li>
                    </ol>
                </section>
